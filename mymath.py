import math

def cos(angle):
    angle = math.radians(angle)
    return math.cos(angle)

def acos(input):
    return math.degrees(math.acos(input))

def sin(angle):
    angle = math.radians(angle)
    return math.sin(angle)

def asin(input):
    return math.degrees(math.asin(input))

def tan(angle):
    angle = math.radians(angle)
    return math.tan(angle)

def atan(input):
    return math.degrees(math.atan(input))

pi = math.pi